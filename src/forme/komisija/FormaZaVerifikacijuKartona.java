    private void btnPronadjiKandidataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPronadjiKandidataActionPerformed
        String kand = txtSifraKandidata.getText();
        if (kand.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite sifru prijave kandidata!");
            return;
        }
        
       

        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.VRATI_KANDIDATA, kand);
        KomunikacijaSaServerom.getInstance().posaljiKZ(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().prihvatiSO();
        kandidat = (Kandidat) so.getOdgovor();
        JOptionPane.showMessageDialog(this, so.getPoruka());


    }//GEN-LAST:event_btnPronadjiKandidataActionPerformed

    private void btnVerifikujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerifikujActionPerformed
        String ime = txtxImeKandidata.getText();
        String prez = txtPrezimeKandidata.getText();

        if (ime.isEmpty() || prez.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Unesite ime i prezime kandidata!");
            return;
        }
        
        if(!kandidat.getIme().toUpperCase().equals(ime.toUpperCase())) {
            JOptionPane.showMessageDialog(this, "Ime kandidata se ne poklapa");
            return;
        }
        if(!kandidat.getPrezime().toUpperCase().equals(prez.toUpperCase())) {
            JOptionPane.showMessageDialog(this, "Prezime kandidata se ne poklapa");
            return;
        }
        karton.setKandidat(kandidat);
        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.SACUVAJ_KARTON_VERIFIKACIJA, karton);
        KomunikacijaSaServerom.getInstance().posaljiKZ(kz);
        ServerskiOdgovor so = KomunikacijaSaServerom.getInstance().prihvatiSO();
        
     JOptionPane.showMessageDialog(this,so.getPoruka());
        

    }//GEN-LAST:event_btnVerifikujActionPerformed

